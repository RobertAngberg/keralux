async function importOrders() {
  console.log(`Importing ${ordersData.length} orders...`);

  for (const orderData of ordersData) {
    try {
      // Determine status based on activities
      let status: "pending" | "shipped" | "completed" | "debt" = "pending";
      const hasFardig = orderData.activities.some((a) => a.details === "Färdig");
      const hasSkickad = orderData.activities.some((a) => a.details === "Skickad");

      if (hasFardig) {
        status = "completed";
      } else if (hasSkickad) {
        status = "shipped";
      }

      // Insert order
      const [order] = await db
        .insert(orders)
        .values({
          orderNumber: orderData.orderNumber,
          email: orderData.email,
          status,
          subtotal: orderData.total,
          total: orderData.total,
          shippingFirstName: orderData.firstName,
          shippingLastName: orderData.lastName,
          shippingAddress: orderData.address,
          shippingCity: orderData.city,
          shippingPostalCode: orderData.postalCode,
          shippingCountry: "Sverige",
          createdAt: new Date(orderData.createdAt),
          updatedAt: new Date(orderData.createdAt),
        })
        .returning();

      // Insert order items
      for (const item of orderData.items) {
        await db.insert(orderItems).values({
          orderId: order.id,
          name: item.name,
          price: "299.00",
          quantity: item.quantity,
        });
      }

      // Insert activities
      for (const activity of orderData.activities) {
        await db.insert(orderActivities).values({
          orderId: order.id,
          action: activity.action,
          details: activity.details,
          createdAt: new Date(activity.createdAt),
        });
      }

      console.log(`✓ Order #${orderData.orderNumber} imported`);
    } catch (error) {
      console.error(`✗ Failed to import order #${orderData.orderNumber}:`, error);
    }
  }

  console.log("\nImport complete!");
  process.exit(0);
}

importOrders();
